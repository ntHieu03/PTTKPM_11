    private void CapNhatMK(String matKhau, String tenDangNhap){
        try{
            String sql="UPDATE TaiKhoan SET matKhau= ? WHERE tenDangNhap=?";
            ps=conn.prepareStatement(sql);
            ps.setString(1, matKhau);
            ps.setString(2, tenDangNhap);
            ps.executeUpdate(); 
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
            try
            {
                String tenDangNhap = tfTenDangNhap.getText();
                String tenHienThi = tfTenHienThi.getText();
                String matKhau = tfMatKhauCu.getText();
                String matKhauMoi = tfMatKhauMoi.getText();
                String nhapLaiMK = tfNhapLaiMatKhau.getText();
                String mkCu = tfMKCu.getText();
                
                if(mkCu.equals(matKhau)){
                    if (!matKhauMoi.equals(nhapLaiMK))
                    {
                        JOptionPane.showMessageDialog(this,"Mật khẩu nhập lại không đúng!");
                    }
                    else
                    {
                        CapNhatMK(matKhauMoi, tenDangNhap);
                        JOptionPane.showMessageDialog(this,"Cập nhật mật khẩu mới thành công!");
                    }
                }else{
                        JOptionPane.showMessageDialog(this,"Mật khẩu cũ không đúng!");
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, JOptionPane.ERROR_MESSAGE);
            }

    }//GEN-LAST:event_btnCapNhatActionPerformed

