    private void tfTenDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfTenDangNhapActionPerformed
        String sql = "SELECT tenHienThi, matKhau FROM TaiKhoan WHERE tenDangNhap = ?";
        try{
            String tenDangNhap = (String) tfTenDangNhap.getText();
            PreparedStatement preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, tenDangNhap);
            ResultSet rs = preparedStatement.executeQuery();
            if(rs.next()){
                tfTenHienThi.setText(rs.getString("tenHienThi"));
               tfMKCu.setText(rs.getString("matKhau"));
            }
        } catch(Exception e){
                JOptionPane.showMessageDialog(this, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tfTenDangNhapActionPerformed

